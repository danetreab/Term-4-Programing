public class Point {
    public int x;
    public int y;
    public Point(){

    }
    public Point(int x,int y){
        this.x=x;
        this.y=y;
    }
    public void setx(int x){
        this.x=x;
    }
    public int getx(){return x;}
    public void sety(int y){
        this.y=y;
    }
    public int gety(){return y;}
}
